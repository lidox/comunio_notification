package de.as.javabot.comunio;

public class Player {
	private int pid;
	private String name;
	private String club;
	private String position;
	private String marketValue;
	private int points;
	private int pointsLastGame;
	private String status;
	private String linkToProfile;
	private String linkToMarketAnalysis;
	private String linkToPhoto;
	
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getClub() {
		return club;
	}
	public void setClub(String club) {
		this.club = club;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getMarketValue() {
		return marketValue;
	}
	public void setMarketValue(String marketValue) {
		this.marketValue = marketValue;
	}
	public int getPoints() {
		return points;
	}
	public void setPoints(int points) {
		this.points = points;
	}
	public int getPointsLastGame() {
		return pointsLastGame;
	}
	public void setPointsLastGame(int pointsLastGame) {
		this.pointsLastGame = pointsLastGame;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getLinkToProfile() {
		return linkToProfile;
	}
	public void setLinkToProfile(String linkToProfile) {
		this.linkToProfile = linkToProfile;
	}
	public String getLinkToMarketAnalysis() {
		return linkToMarketAnalysis;
	}
	public void setLinkToMarketAnalysis(String linkToMarketAnalysis) {
		this.linkToMarketAnalysis = linkToMarketAnalysis;
	}
	public String getLinkToPhoto() {
		return linkToPhoto;
	}
	public void setLinkToPhoto(String linkToPhoto) {
		this.linkToPhoto = linkToPhoto;
	}
	
	
}
